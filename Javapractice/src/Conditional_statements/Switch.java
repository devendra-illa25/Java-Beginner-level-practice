package Conditional_statements;

public class Switch {
public static void main(String[] args) {
	int i=3;
	switch (i) {
	case 1:
		// statement
		System.out.println("Hello");
	break;
	
	case 2:
		//statement
		System.out.println("Good Morning");
		break;
		
	case 3:
		//statement
		System.out.println("Devendra");
		break;
	
		default:
			System.out.println("Chaitanya");
			break;
			
	
	}
}
}

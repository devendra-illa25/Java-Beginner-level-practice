package ControlStatements;

public class if_else_if {

	public static void main(String[] args)
	{
		int z=10;
		if(z<5)
		{
			System.out.println("z is less than 5");
		}
		else if(z==5)
		{
			System.out.println("Z is equal to 5");
		}
		else 
		{
			System.out.println("Z is greater than 5");
		}
	}
}
